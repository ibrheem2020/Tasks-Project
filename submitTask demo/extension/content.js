function getTaskUrl() {
  let pcodeSearchTerm = "worker";
  let fixedDataSearchTerms = [
    "bit",
    "t.ly",
    "tiny",
    "tinyurl",
    "liik",
    "google",
    "blogspot",
    "cutt",
    "bit.ly",
    "snipboard.io",
    "t.co",
    "rebrand.ly",
    "qr.ae",
    "s.id",
    "psee.io",
    "rb.gy",
    "shorturl.at",
  ];
  let result = {
    pcodeUrl: null,
    fixedDataUrl: null,
  };
  let instructionsText = getInstructionsText();
  const urlRegex = /(https?:\/\/[^\s]+)/g;
  let match;
  while ((match = urlRegex.exec(instructionsText)) !== null) {
    const url = match[0].trim();
    if (!result.pcodeUrl && url.indexOf(pcodeSearchTerm) !== -1) {
      result.pcodeUrl = url;
    } else {
      for (let i = 0; i < fixedDataSearchTerms.length; i++) {
        const fixedDataSearchTerm = fixedDataSearchTerms[i];
        if (!result.fixedDataUrl && url.indexOf(fixedDataSearchTerm) !== -1) {
          result.fixedDataUrl = url;
        }
      }
    }
  }
  return result;
}

