function getInstructionsText() {
  var instructionsText = document.getElementById("job-instructions");
  function extractText(node, result) {
    if (node.nodeType === 3) {
      result.push(node.textContent.trim());
    } else if (node.nodeType === 1) {
      for (var i = 0; i < node.childNodes.length; i++) {
        extractText(node.childNodes[i], result);
      }
    }
  }
  var allText = [];
  for (var i = 0; i < instructionsText.childNodes.length; i++) {
    extractText(instructionsText.childNodes[i], allText);
  }
  let text = allText.join(" ");
  return text;
}

function changeGclid() {
  function generateRandomString(length) {
    const characters = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789-_";
    let genText = "";

    for (let i = 0; i < length; i++) {
      const randomIndex = Math.floor(Math.random() * characters.length);
      genText += characters.charAt(randomIndex);
    }

    return genText;
  }

  // Possible values to replace randomValue
  const possibleValues = ["vD_BwE", "PD_BwE", "_D_BwE", "fD_BwE"];

  // Randomly select one value from the array
  const randomIndex = Math.floor(Math.random() * possibleValues.length);
  const randomValue = possibleValues[randomIndex];

  // Generate a random string with the same length as the example
  const randomCode = "EAIaIQobChMI" + generateRandomString(37) + randomValue;

  return randomCode;
}

function replaceGclid(originalUrl) {
  let replacedUrl = originalUrl.replace(/gclid=[^&]*/, `gclid=${changeGclid()}`);
  return replacedUrl;
}

